const exactPageMetadata = new Map();

for (const route of ROUTE_METADATA) {
  for (const exactPath of route.exactPaths) {
    exactPageMetadata.set(exactPath, route);
  }
}

export function getPageMetadata(pathname) {
  const exactMatch = exactPageMetadata.get(pathname);

  if (exactMatch) {
    return exactMatch;
  }

  for (const route of ROUTE_METADATA) {
    if (route.matchers?.some((matcher) => matcher.test(pathname))) {
      return route;
    }
  }

  return DEFAULT_PAGE_METADATA;
}

export function getBotPreviewRewrites() {
  return ROUTE_METADATA.flatMap((route) =>
    route.botSources.map((source) => ({
      source,
      destination: `/bot-previews/${route.key}.html`,
    })),
  );
}
